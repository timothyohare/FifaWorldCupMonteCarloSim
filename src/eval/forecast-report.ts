// Runnable forecast-verification report: scores every day of history/champion-odds.csv
// against the realised outcomes and writes history/forecast-vs-reality.md.
//   tsx src/eval/forecast-report.ts [--permutations N] [--out path]
// Group qualifiers are derived from the committed snapshot via the engine's own
// tiebreakers; knockout outcomes come from fixtures/wc2026-outcomes.json. Markets that
// have not resolved yet (e.g. champion before the final) are skipped, so re-running
// after each round upgrades the report.
import { readFileSync, writeFileSync } from "node:fs";
import { mulberry32 } from "../domain/rng";
import type { TeamId } from "../domain/types";
import { selectQualifiers } from "../engine/group-engine";
import { rankGroup } from "../engine/standings";
import { fromKickpoolSnapshot } from "../io/snapshot";
import {
  auc,
  brierBinary,
  brierSkillScore,
  calibrationBins,
  MARKETS,
  parseOddsCsv,
  permutationPValue,
  resolveMarkets,
  type MarketName,
  type OddsHistory,
  type OutcomesFixture,
} from "./forecast-verify";

const SEED = 20260714; // fixed seed: the permutation p-values must reproduce run-to-run

/** Rank every fully played group and apply the best-thirds rule — the realised 32. */
export function qualifiersFromSnapshot(snapshotPath: string): TeamId[] {
  const snapshot = JSON.parse(readFileSync(snapshotPath, "utf8"));
  const input = fromKickpoolSnapshot(snapshot, { bestThirds: 8 });
  const results = input.groups.map((g) => {
    if (g.remaining.length > 0) {
      throw new Error(`group ${g.group} still has unplayed matches — qualifiers are not final`);
    }
    return { group: g.group, table: rankGroup(g.teams, g.played) };
  });
  const q = selectQualifiers(results, { bestThirds: 8 });
  return [...q.winners, ...q.runnersUp, ...q.bestThirds];
}

const MARKET_LABELS: Record<MarketName, string> = {
  champion: "Champion",
  runnerUp: "Runner-up",
  reachFinal: "Reach the final",
  reachSemi: "Reach the semi-finals",
  escapeGroup: "Escape the group",
};

const pctf = (v: number) => `${(v * 100).toFixed(1)}%`;

/** 1/(N+1) is the test's floor — report it as an inequality, and small p in exponential form. */
const pf = (p: number, permutations: number) => {
  if (p <= 1 / (permutations + 1) + 1e-12) return `< ${(1 / permutations).toExponential(0)}`;
  return p < 1e-3 ? p.toExponential(1) : p.toFixed(4);
};

export function buildReport(
  history: OddsHistory,
  outcomes: OutcomesFixture,
  qualifiers: TeamId[],
  permutations: number,
): string {
  const markets = resolveMarkets(outcomes, qualifiers);
  const resolved = MARKETS.filter((m) => markets[m] !== undefined);
  const pending = MARKETS.filter((m) => markets[m] === undefined);
  const lines: string[] = [];

  lines.push("# Forecast vs reality — World Cup 2026");
  lines.push("");
  lines.push(
    `Generated by \`src/eval/forecast-report.ts\` (seed ${SEED}, ${permutations.toLocaleString("en-GB")} ` +
      `permutations). Scores the ${history.dates.length} daily captures in ` +
      `\`history/champion-odds.csv\` (${history.dates[0]} → ${history.dates[history.dates.length - 1]}) ` +
      `against realised outcomes as of ${outcomes.asOf}.`,
  );
  if (pending.length > 0) {
    lines.push("");
    lines.push(
      `Not yet resolvable: ${pending.map((m) => MARKET_LABELS[m].toLowerCase()).join(", ")} — ` +
        "fill in `fixtures/wc2026-outcomes.json` after those matches and re-run.",
    );
  }

  for (const m of resolved) {
    const achieved = markets[m]!;
    lines.push("");
    lines.push(`## ${MARKET_LABELS[m]}`);
    lines.push("");
    lines.push(
      `${achieved.size} of ${history.teams.length} teams achieved this: ` +
        `${[...achieved].sort().join(", ")}.`,
    );
    lines.push("");
    lines.push("| Date | Brier | Skill vs base rate | AUC | Permutation p |");
    lines.push("|---|---|---|---|---|");
    for (const d of history.dates) {
      const ps = history.teams.map((t) => history.rows[d][t][m]);
      const ys = history.teams.map((t) => (achieved.has(t) ? 1 : 0));
      const p = permutationPValue(ps, ys, { permutations, rng: mulberry32(SEED) });
      lines.push(
        `| ${d} | ${brierBinary(ps, ys).toFixed(4)} | ${brierSkillScore(ps, ys).toFixed(3)} | ` +
          `${auc(ps, ys).toFixed(3)} | ${pf(p, permutations)} |`,
      );
    }

    // Most confident wrong calls on the final capture, both directions.
    const last = history.dates[history.dates.length - 1];
    const day = history.teams.map((t) => ({ t, p: history.rows[last][t][m], hit: achieved.has(t) }));
    const falseIn = day.filter((x) => !x.hit).sort((a, b) => b.p - a.p)[0];
    const falseOut = day.filter((x) => x.hit).sort((a, b) => a.p - b.p)[0];
    lines.push("");
    lines.push(
      `Final capture's most confident miss: **${falseIn.t}** at ${pctf(falseIn.p)} did not achieve it; ` +
        `least fancied achiever: **${falseOut.t}** at ${pctf(falseOut.p)}.`,
    );
  }

  // Calibration is only meaningful for the market with events in every band.
  const eg = markets.escapeGroup!;
  const ps: number[] = [];
  const ys: number[] = [];
  for (const d of history.dates) {
    for (const t of history.teams) {
      ps.push(history.rows[d][t].escapeGroup);
      ys.push(eg.has(t) ? 1 : 0);
    }
  }
  lines.push("");
  lines.push("## Calibration — escape the group, all days pooled");
  lines.push("");
  lines.push(
    "Descriptive only: the same tournament underlies every day, so the team-days are correlated.",
  );
  lines.push("");
  lines.push("| Predicted band | n | Mean predicted | Observed rate |");
  lines.push("|---|---|---|---|");
  for (const b of calibrationBins(ps, ys, 5)) {
    if (b.n === 0) continue;
    lines.push(
      `| ${pctf(b.lo)}–${pctf(b.hi)} | ${b.n} | ${pctf(b.meanPredicted)} | ${pctf(b.observed)} |`,
    );
  }

  lines.push("");
  lines.push("## Reading the numbers");
  lines.push("");
  lines.push(
    "- **Brier** — mean squared error of the probabilities; proper, so it cannot be gamed by hedging.",
  );
  lines.push(
    "- **Skill vs base rate** — `1 − Brier(model)/Brier(base rate)`; 0 is no better than " +
      "forecasting every team at the base rate, 1 is perfect.",
  );
  lines.push(
    "- **AUC** — how often a randomly chosen achiever was priced above a randomly chosen non-achiever.",
  );
  lines.push(
    "- **Permutation p** — the forecasts are held fixed and the k-of-n outcome labels reshuffled; " +
      "this respects the contested-slots structure that a binomial test would ignore.",
  );
  lines.push(
    "- **Caveats** — one tournament only (the out-of-sample evidence is `src/eval/calibrate.ts`); " +
      "the daily captures share information, so read the per-day p-values as one signal, not many.",
  );
  lines.push("");
  return lines.join("\n");
}

function main(): void {
  const args = process.argv.slice(2);
  const flag = (name: string): string | undefined => {
    const i = args.indexOf(name);
    return i >= 0 ? args[i + 1] : undefined;
  };
  const permutations = Number(flag("--permutations") ?? 100_000);
  const out = flag("--out") ?? "history/forecast-vs-reality.md";

  const history = parseOddsCsv(readFileSync("history/champion-odds.csv", "utf8"));
  const outcomes: OutcomesFixture = JSON.parse(readFileSync("fixtures/wc2026-outcomes.json", "utf8"));
  const qualifiers = qualifiersFromSnapshot("fixtures/wc2026-snapshot.json");

  const report = buildReport(history, outcomes, qualifiers, permutations);
  writeFileSync(out, report);
  console.log(`wrote ${out}`);
}

const isDirectRun = process.argv[1]?.endsWith("forecast-report.ts");
if (isDirectRun) main();
